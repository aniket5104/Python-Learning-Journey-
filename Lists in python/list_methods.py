l1=[5,6,98,45,65,2]

#1. sort ---> sorts in ascending order
l1.sort()
print(l1)

#2. reverse ---> reverses a list
l1.reverse()
print(l1)

#3. append(n) ---> Adds n at last of the string
l1.append(8)
print(l1)

#4. insert(3,8) ---> At 3rd index inserts 8
l1.insert(3,8)
print(l1)

#5. pop(2) ---> will delete element at 2nd index and return its value
print(l1.pop(2))
print(l1)

#6. remove(21) ---> Will remove 21 from the list
l1.remove(98)
print(l1)